n1=int(input("Digite um número:"))
n2=int(input("Digie outro número:"))

o=input("escolha uma operação (+,-,/,*) ")
if o=="+":
    res=n1+n2
    print(res)
elif o=="-":
    res=n1-n2
    print(res)
elif o=="*":
    res=n1-n2
    print(res)
elif o=="/":
    res=n1-n2
    print(res)
else:
    print("Operação invalida")